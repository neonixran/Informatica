    /**
     * Carga la lista de países y los agrega al combobox cmbPais.
     */
    public final void cargarPaises() { // Función para cargar la lista de países.
        try {
            Conector conector = new Conector(); // Se instancia la clase que establece la conexión con la base de datos.

            ResultSet rs = conector.consultar("SELECT Name FROM Country ORDER BY Name");

            while (rs.next()) { // Recorre los resultados mientra haya datos.
                cmbPais.addItem(rs.getString("Name")); // Añade cada nombre del país al combobox.
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Carga de países (SQL ERROR)", JOptionPane.WARNING_MESSAGE);
        }
    }

    /**
     * Obtiene el código del país seleccionado (por ejemplo, CHL para Chile).
     *
     * @return El código del país seleccionado, o un string vacío si no hay
     * selección o ocurre un error.
     */
    public String obtenerCodigoPais() { // Función para obtener el código del país seleccionado (ej. CHL)
        if (cmbPais.getSelectedItem() == null) { // Se valida que el item en cmbPais no sea nulo por alguna razón, al inicio del programa tal vez.
            return "";
        }

        String pais = cmbPais.getSelectedItem().toString(); // Se obtiene el país seleccionado, además de convertirlo a texto ya que por si solo viene como Objeto.

        try {
            Conector conector = new Conector(); // Se instancia la clase que establece la conexión con la base de datos.

            ResultSet rs = conector.consultar("SELECT Code FROM Country WHERE Name = ?", pais); // Consulta SQL que devuelve el código del país, pasando como argumento el nombre del país.

            if (rs.next()) { // Se verifica si la consulta devolvió datos.
                return rs.getString("Code"); // Retorna el código del país.
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Código de país (SQL ERROR)", JOptionPane.WARNING_MESSAGE);
        }

        return ""; // Por defecto retorna un string vacío.
    }

    /**
     * Carga los distritos de Chile y los agrega al combobox cmbDistrito.
     */
    public final void cargarDistritosCHL() { // Función que carga los distritos de Chile en el combobox cmbDistrito.
        try {
            Conector conector = new Conector(); // Se instancia la clase que establece la conexión con la base de datos.

            ResultSet rs = conector.consultar("SELECT DISTINCT(DISTRICT) FROM city WHERE CountryCode='CHL'");

            while (rs.next()) { // Recorre los resultados mientra haya datos.
                cmbDistrito.addItem(rs.getString("DISTRICT")); // Añade cada nombre del distrito al combobox.
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Carga de distritos (SQL ERROR)", JOptionPane.WARNING_MESSAGE);
        }
    }

