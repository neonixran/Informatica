    /**
     * Botón para insertar la ciudad ingresada por el usuario.
     */
    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        String ciudad = txtCiudad.getText(); // Se obtiene la ciudad ingresada.
        String distrito = cmbDistrito.getSelectedItem().toString(); // Se obtiene el distrito seleccionado, además de convertirlo a texto ya que por si solo viene como Objeto.

        if (ciudad.equals("")) { // Se valida que la ciudad ingresada no esté vacia, de ser así se muestra un mensaje al usuario.
            JOptionPane.showMessageDialog(null, "Debe ingresar una ciudad", "Ciudad", JOptionPane.WARNING_MESSAGE);
            return; // Se devuelve para que el código no continue avanzando, de no agregar el return pasaria de todas forma a intentar agregar la ciudad. (PD: También se puede lograr envolviendo el try-catch [línea 245 a 266] en un else)
        }

        try {
            int poblacion = Integer.parseInt(txtPoblacion.getText()); // Se obtiene la población ingresada y se convierte a número entero.

            if (poblacion < 0) { // Se valida que no sea menor a 0
                JOptionPane.showMessageDialog(null, "Debe ingresar una cantidad mayor a 0", "Población", JOptionPane.WARNING_MESSAGE);
                return; // Se devuelve para que el código no continue avanzando, de no agregar el return pasaria de todas forma a intentar agregar la ciudad. (PD: También se puede lograr envoviendo la conexión e inserción [línea 253-261] en un else)
            }

            Conector conector = new Conector(); // Se instancia la clase que establece la conexión con la base de datos.

            int filasActualizadas = conector.modificar("INSERT INTO city (Name, CountryCode, District, Population) VALUES (?, 'CHL', ?, ?);", ciudad, distrito, poblacion);

            if (filasActualizadas > 0) { // Se valida que la cantidad de filas actualizadas sea mayor que 0, esto para saber si se agrego la ciudad o no.
                JOptionPane.showMessageDialog(null, "Se ingreso con exito la ciudad", "Resultado", JOptionPane.PLAIN_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "No se ingreso la ciudad", "Resultado", JOptionPane.WARNING_MESSAGE);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Inserción de ciudad (SQL ERROR)", JOptionPane.WARNING_MESSAGE);
        } catch (NumberFormatException e) { // Si la población ingresada es un texto, string vacio se lanza una excepción para que el programa no se rompa.
            JOptionPane.showMessageDialog(null, "Ingrese un número válido para la población", "Población", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnInsertarActionPerformed

