    DefaultTableModel mt = new DefaultTableModel(); // Se instancia el DefaultTableModel (Modelo de tabla?)
    private void cmbPaisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbPaisActionPerformed
        mt.setRowCount(0); // Se elimina todas las filas de la tabla, esto para que al seleccionar otro país se borren las filas que ya estén.

        String pais = obtenerCodigoPais(); // Se llama a la función que devuelve el código del país seleccionado.

        String columnas[] = {"ID", "Nombre", "País", "Distrito", "Población"}; // Se crea un arreglo que almacena los encabezados de cada columna.
        mt.setColumnIdentifiers(columnas); // Se añaden al modelo de la tabla instanciado anteriormente.
        tbCiudades.setModel(mt); // Se agrega el modelo a la tabla de Ciudades.

        try {
            Conector conector = new Conector(); // Se instancia la clase que establece la conexión con la base de datos.

            ResultSet rs = conector.consultar("SELECT ID, Name, CountryCode, District, Population FROM city WHERE CountryCode = ? ORDER BY Name", pais);

            while (rs.next()) { // Recorre los resultados mientra haya datos.
                mt.addRow(new Object[]{
                    rs.getString("ID"),
                    rs.getString("Name"),
                    rs.getString("CountryCode"),
                    rs.getString("District"),
                    rs.getString("Population")
                }); // Se van agregando filas con los datos.
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Carga de ciudades (SQL ERROR)", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_cmbPaisActionPerformed

