    private void cargarPaises(JComboBox<String> componente) {
        try {
            Conector conector = new Conector();

            ResultSet rs = conector.consultar("SELECT Name FROM country ORDER BY Name");

            while (rs.next()) {
                componente.addItem(rs.getString("Name"));
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "SQL ERROR", JOptionPane.WARNING_MESSAGE);
        }
    }


