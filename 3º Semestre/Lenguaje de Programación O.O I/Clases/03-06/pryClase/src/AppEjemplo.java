    private void btnP_ModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnP_ModificarActionPerformed
        if (cmbP_Pais.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(null, "No hay un país seleccionado", "Modificación", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String pais = cmbP_Pais.getSelectedItem().toString();

        try {
            float gnp = Float.parseFloat(txtP_GNP.getText());

            if (gnp < 1) {
                JOptionPane.showMessageDialog(null, "Debe ingresar un GNP mayor a 0.", "Advertencia", JOptionPane.WARNING_MESSAGE);
                return;
            }

            Conector conector = new Conector();

            int cant = conector.modificar("UPDATE country SET GNPOld = GNP, GNP = ? WHERE Name = ?;", gnp, pais);

            if (cant > 0) {
                JOptionPane.showMessageDialog(null, "El GNP fue actualizado exitosamente.", "Modificación", JOptionPane.INFORMATION_MESSAGE);

                cargarDatosPais();
            } else {
                JOptionPane.showMessageDialog(null, "El GNP no fue actualizado.", "Modificación", JOptionPane.WARNING_MESSAGE);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "SQL ERROR", JOptionPane.WARNING_MESSAGE);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Ingrese un número válido", "Formato", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnP_ModificarActionPerformed

    private void cargarDatosPais() {
        if (cmbP_Pais.getSelectedItem() == null) {
            return;
        }

        String pais = cmbP_Pais.getSelectedItem().toString();

        try {
            Conector conector = new Conector();

            ResultSet rs = conector.consultar("SELECT GNP, GNPOld FROM country WHERE Name = ?", pais);

            if (rs.next()) {
                txtP_GNP.setText(rs.getString("GNP"));
                txtP_GNPOld.setText(rs.getString("GNPOld"));
            } else {
                JOptionPane.showMessageDialog(null, "No se encontraron resultados para el país seleccionado.", "Carga datos", JOptionPane.WARNING_MESSAGE);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "SQL ERROR", JOptionPane.WARNING_MESSAGE);
        }
    }
    
