    private void cmbC_PaisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbC_PaisActionPerformed
        cmbC_Ciudad.removeAllItems();
        
        try {
            Conector conector = new Conector();
            
            String pais = cmbC_Pais.getSelectedItem().toString();
            
            ResultSet rs = conector.consultar("SELECT c.Name FROM city c JOIN country ct ON(c.CountryCode = ct.Code) WHERE ct.Name  = ?", pais);
            
            while (rs.next()) {
                cmbC_Ciudad.addItem(rs.getString("Name"));
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "SQL ERROR", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_cmbC_PaisActionPerformed

