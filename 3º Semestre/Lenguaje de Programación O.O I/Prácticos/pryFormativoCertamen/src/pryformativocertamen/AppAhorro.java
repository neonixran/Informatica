    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        try {
            double montoMensual = Double.parseDouble(txtMensual.getText());
            double metaAhorro = Double.parseDouble(txtMeta.getText());
            int cantMeses = Integer.parseInt(txtNumMeses.getText());

            Ahorro ahorro = new Ahorro(montoMensual, metaAhorro, cantMeses);
            
            double diferencia = ahorro.obtenerDiferencia();
            
            if (diferencia == 0) {
                JOptionPane.showMessageDialog(null, "Meta alcanzada justo en el plazo previsto.", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
            } else if (diferencia > 0) {
                JOptionPane.showMessageDialog(null, "¡Meta alcanzada con un excedente de $" + diferencia, "Mensaje", JOptionPane.INFORMATION_MESSAGE);
            } else if (diferencia < 0) {
                JOptionPane.showMessageDialog(null, "No alcanza la meta. Le faltan $" + (diferencia * -1), "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                
                String res = JOptionPane.showInputDialog("¿Desea recalcular? Ingrese un nuevo número de meses:");
                
                System.out.println(res);
            }
        } catch (ArithmeticException e) {
            System.out.println(e.getMessage());
        } catch(Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

